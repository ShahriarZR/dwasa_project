CREATE TABLE WASTAGE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID NUMBER(19) NOT NULL,
    INGRADIENT_ID NUMBER(19) NOT NULL,
    UNIT NUMBER(10) NOT NULL,
    REMARKS CLOB,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CREATED_BY NUMBER(19),
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY NUMBER(19),
    DELETED_AT TIMESTAMP WITH TIME ZONE,
    CONSTRAINT FK_WASTAGE_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT_TYPE (ID),
    CONSTRAINT FK_WASTAGE_INGRADIENT FOREIGN KEY (INGRADIENT_ID) REFERENCES INGRADIENT (ID)
);