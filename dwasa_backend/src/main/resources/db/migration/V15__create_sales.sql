BEGIN
    EXECUTE IMMEDIATE '
        CREATE TABLE SALES (
            ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            PRODUCT_ID NUMBER(19) NOT NULL,
            RATE_PER_PCS NUMBER(12, 2) NOT NULL,
            RATE_PER_CASE NUMBER(12, 2) NOT NULL,
            MINIMUM_QUANTITY NUMBER(10) NOT NULL,
            PRODUCTION_COST NUMBER(12, 2) NOT NULL,
            PRODUCTION_COST_BASIS NUMBER(10) NOT NULL,
            VAT NUMBER(10, 2) NOT NULL,
            SD NUMBER(10, 2) NOT NULL,
            APPLIES_IN NUMBER(10) NOT NULL,
            STATUS NUMBER(10) NOT NULL,
            CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
            CREATED_BY NUMBER(19),
            UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
            UPDATED_BY NUMBER(19),
            DELETED_AT TIMESTAMP WITH TIME ZONE,
            CONSTRAINT FK_SALES_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT_TYPE (ID)
        )';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
            RAISE;
        END IF;
END;
/
