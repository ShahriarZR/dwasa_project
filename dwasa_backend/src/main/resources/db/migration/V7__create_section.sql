CREATE TABLE SECTION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(150) NOT NULL,
    LOCATION VARCHAR2(255),
    REMARKS CLOB
);

CREATE TABLE SECTION_INGRADIENT_UNIT (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SECTION_ID NUMBER NOT NULL,
    FLOW_TYPE VARCHAR2(10) NOT NULL,
    INGRADIENT_ID NUMBER NOT NULL,
    UNIT_ID NUMBER NOT NULL,
    CONSTRAINT FK_SECTION_ITEM_SECTION FOREIGN KEY (SECTION_ID) REFERENCES SECTION (ID) ON DELETE CASCADE,
    CONSTRAINT FK_SECTION_ITEM_INGRADIENT FOREIGN KEY (INGRADIENT_ID) REFERENCES INGRADIENT (ID),
    CONSTRAINT FK_SECTION_ITEM_UNIT FOREIGN KEY (UNIT_ID) REFERENCES UNIT (ID)
);

ALTER TABLE SECTION_INGRADIENT_UNIT
    ADD CONSTRAINT CK_SECTION_ITEM_FLOW_TYPE CHECK (FLOW_TYPE IN ('INPUT', 'OUTPUT'));