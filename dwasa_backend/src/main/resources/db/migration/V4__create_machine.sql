CREATE TABLE MACHINE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MACHINE_NAME VARCHAR2(150) NOT NULL,
    SECTION_NAME VARCHAR2(150),
    LOCATION VARCHAR2(255),
    BRAND VARCHAR2(150),
    REMARKS CLOB,
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CREATED_BY NUMBER(19),
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY NUMBER(19),
    DELETED_AT TIMESTAMP WITH TIME ZONE
);

ALTER TABLE MACHINE
    ADD CONSTRAINT CK_MACHINE_STATUS CHECK (STATUS IN ('ACTIVE', 'INACTIVE'));

ALTER TABLE MACHINE
    ADD CONSTRAINT CK_MACHINE_SECTION_NAME CHECK (
        SECTION_NAME IS NULL OR SECTION_NAME IN (
            'Preform',
            'Cap',
            'Hanger',
            'Bottle',
            'Filling & Wrapping',
            'Send to floor'
        )
    );
