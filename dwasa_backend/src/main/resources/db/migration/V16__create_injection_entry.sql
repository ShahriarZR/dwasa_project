BEGIN
    EXECUTE IMMEDIATE '
        CREATE TABLE INJECTION_ENTRY (
            ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            SECTION_ID NUMBER(19) NOT NULL,
            ENTRY_DATE DATE NOT NULL,
            ENTRY_TIME VARCHAR2(8) NOT NULL,
            REMARKS CLOB,
            CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
            CREATED_BY NUMBER(19),
            UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
            UPDATED_BY NUMBER(19),
            DELETED_AT TIMESTAMP WITH TIME ZONE,
            DELETED_BY NUMBER(19),
            CONSTRAINT FK_INJ_ENTRY_SECTION FOREIGN KEY (SECTION_ID) REFERENCES SECTION (ID),
            CONSTRAINT CK_INJ_ENTRY_TIME CHECK (REGEXP_LIKE(ENTRY_TIME, ''^([01][0-9]|2[0-3]):[0-5][0-9](:[0-5][0-9])?$''))
        )';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
            RAISE;
        END IF;
END;
/

BEGIN
    EXECUTE IMMEDIATE '
        CREATE TABLE INJECTION_ENTRY_ITEM (
            ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            INJECTION_ENTRY_ID NUMBER(19) NOT NULL,
            SECTION_INGRADIENT_UNIT_ID NUMBER(19) NOT NULL,
            INGRADIENT_ID NUMBER(19) NOT NULL,
            UNIT_ID NUMBER(19) NOT NULL,
            FLOW_TYPE VARCHAR2(10) NOT NULL,
            QUANTITY NUMBER(18, 3) NOT NULL,
            CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
            CREATED_BY NUMBER(19),
            UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
            UPDATED_BY NUMBER(19),
            DELETED_AT TIMESTAMP WITH TIME ZONE,
            DELETED_BY NUMBER(19),
            CONSTRAINT FK_INJ_ENTRY_ITEM_ENTRY FOREIGN KEY (INJECTION_ENTRY_ID) REFERENCES INJECTION_ENTRY (ID) ON DELETE CASCADE,
            CONSTRAINT FK_INJ_ENTRY_ITEM_SECTION_ITEM FOREIGN KEY (SECTION_INGRADIENT_UNIT_ID) REFERENCES SECTION_INGRADIENT_UNIT (ID),
            CONSTRAINT FK_INJ_ENTRY_ITEM_INGRADIENT FOREIGN KEY (INGRADIENT_ID) REFERENCES INGRADIENT (ID),
            CONSTRAINT FK_INJ_ENTRY_ITEM_UNIT FOREIGN KEY (UNIT_ID) REFERENCES UNIT (ID),
            CONSTRAINT CK_INJ_ENTRY_ITEM_FLOW CHECK (FLOW_TYPE IN (''INPUT'', ''OUTPUT'')),
            CONSTRAINT CK_INJ_ENTRY_ITEM_QTY CHECK (QUANTITY >= 0),
            CONSTRAINT UK_INJ_ENTRY_ITEM UNIQUE (INJECTION_ENTRY_ID, SECTION_INGRADIENT_UNIT_ID)
        )';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
            RAISE;
        END IF;
END;
/