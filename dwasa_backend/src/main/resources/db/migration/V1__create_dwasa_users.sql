CREATE TABLE DWASA_USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(150) NOT NULL,
    DEPARTMENT VARCHAR2(50) NOT NULL,
    DESIGNATION VARCHAR2(100),
    ROOT_ID NUMBER(19),
    EMP_ID VARCHAR2(50) NOT NULL,
    MOBILE_NO VARCHAR2(20),
    EMAIL VARCHAR2(150),
    JOIN_DATE DATE,
    BIRTH_DATE DATE,
    RELIGION VARCHAR2(50),
    GENDER VARCHAR2(20),
    ADDRESS CLOB,
    REMARKS CLOB,
    PASSWORD VARCHAR2(255) NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL
);

ALTER TABLE DWASA_USERS ADD CONSTRAINT UK_DWASA_USERS_EMP_ID UNIQUE (EMP_ID);
ALTER TABLE DWASA_USERS ADD CONSTRAINT UK_DWASA_USERS_EMAIL UNIQUE (EMAIL);

ALTER TABLE DWASA_USERS ADD CONSTRAINT CK_DWASA_USERS_DEPT CHECK (
    DEPARTMENT IN (
        'ADMIN',
        'HR',
        'IT',
        'FINANCE',
        'ACCOUNTS',
        'ENGINEERING',
        'MANAGEMENT',
        'OTHER'
    )
);

ALTER TABLE DWASA_USERS ADD CONSTRAINT CK_DWASA_USERS_GENDER CHECK (
    GENDER IN ('MALE', 'FEMALE', 'OTHER')
);
